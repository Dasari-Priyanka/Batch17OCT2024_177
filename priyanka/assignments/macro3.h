// geometry.h
#ifndef GEOMETRY_H  // Header guard to avoid multiple inclusions
#define GEOMETRY_H

// Define constant for PI
#define PI 3.14159

// Macro to calculate area of a circle
#define AREA_CIRCLE(radius) (PI * (radius) * (radius))

// Macro to calculate area of a triangle
#define AREA_TRIANGLE(base, height) (0.5 * (base) * (height))

#endif // GEOMETRY_H

